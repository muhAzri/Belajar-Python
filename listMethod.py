numbers = [1,3,2,6,3]
print (numbers)

# Menambahkan Data Kedalam List
numbers.append(99)
print(numbers)

# Menambahkan Data kedalam list dengan index tertentu
numbers.insert(1, 24)
print (numbers)

# Menghapus data dari dalam list

numbers.pop(1)
print (numbers)

numbers.remove(99)
print (numbers)

# Sort data
numbers.sort()
print (numbers)