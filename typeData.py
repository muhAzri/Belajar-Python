# Variabel Adalah suatu benda untuk menyimpan suatu value

# Integer
price = 100000
print (price)

# Float / Double
discount = 0.5

print (discount * 100)

# String
product_name = 'Something'
print (product_name)

# Boolean (True/False) Conditional
is_discount = True
print (is_discount)

# 
price = price * discount
print (price)